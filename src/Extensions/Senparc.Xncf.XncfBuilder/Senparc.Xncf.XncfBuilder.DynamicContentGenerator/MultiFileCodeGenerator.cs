```
"";
");


        sb.AppendLine(@"public const string FrontendTemplate = @$""
## Page UI sample (front-end)
File Name: DatabaseSampleIndex.cshtml
File Path: < ModuleRootPath >/ Areas / Admin / Pages / Template_XncfName
Code:
